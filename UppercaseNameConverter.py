#Prog03: Create a program that ask the user to input their fullname. Print the input in all capital letter.
#Example:
#Input: Juan Dela Cruz
#Output: JUAN DELA CRUZ

# Ask the user to input their fullname
fullname = input("Enter your fullname: ")
# Convert the input to uppercase
uppercase_fullname = fullname.upper()
# Print the input in all capital letters
print(f"Your fullname in uppercase is: {uppercase_fullname}")
