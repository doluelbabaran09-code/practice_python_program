# Prog03: Create a program that ask user to input 2 numbers. Print the difference of the two numbers.

# Input: Get two numbers and convert them to floats
num_1 = float(input("Enter the first number: "))
num_2 = float(input("Enter the second number: "))

# Logic: Use the subtraction operator (-)
difference_of_numbers = num_1 - num_2

# Output: Print the result
print(f"The difference of the two numbers is: {difference_of_numbers}")