#Prog04: Create a program that ask the user to input their fullname. Print the input in all lower case.
#Example:
#Input: Juan Dela Cruz
#Output: juan dela cruz

# Ask the user to input their fullname
fullname = input("Enter your fullname: ")
# Convert the input to lowercase
lowercase_fullname = fullname.lower()
# Print the input in all lowercase letters
print(f"Your fullname in lowercase is: {lowercase_fullname}")   