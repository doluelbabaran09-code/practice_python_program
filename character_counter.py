#Prog08: Create a program that ask the user to input their fullname. Print the number of characters in the input.
#Example:
#Input: Juan Dela Cruz
#Output: 14

# Ask the user to input their fullname
fullname = input("Enter your fullname: ")
# Count the number of characters in the input
character_count = len(fullname)
# Print the number of characters in the input
print(f"The number of characters in your fullname is: {character_count}")
